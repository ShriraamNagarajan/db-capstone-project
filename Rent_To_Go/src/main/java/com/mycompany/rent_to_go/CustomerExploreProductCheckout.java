    public boolean checkEmptyFields(){
        if(txtName.getText().length()==0 || txtNum.getText().length()==0 || txtDate.getText().length()==0 || txtCvv.getText().length()==0) {
            return true;
        }
        else{
            return false;
        }
    }
    public boolean agreementCheck(){
        if(!chkTerm1.isSelected() || !chkTerm2.isSelected() || !chkRefund.isSelected()){
            return false;
        }
        return true;
    }
