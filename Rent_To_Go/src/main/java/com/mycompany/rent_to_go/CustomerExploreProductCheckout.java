    private void btnProceedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProceedActionPerformed

         if(checkEmptyFields()){
                JOptionPane.showMessageDialog(null, "Please enter all the required details", "title", JOptionPane.PLAIN_MESSAGE);

         }else if(lblNumberError.isVisible()||lblDateError.isVisible() || lblCvvError.isVisible()){
                JOptionPane.showMessageDialog(null, "Please enter valid details before proceeding", "title", JOptionPane.PLAIN_MESSAGE);
             
         }
         
         else if(!agreementCheck()){
                JOptionPane.showMessageDialog(null, "You need to agree to our terms and conditions in order to complete the rental process.", "title", JOptionPane.PLAIN_MESSAGE);
             
         }else{
             completeBooking();
         }
     
 
    }//GEN-LAST:event_btnProceedActionPerformed

    
    public void completeBooking(){
        String paymentDate = LocalDate.now().format(DateTimeFormatter.ofPattern("dd/MM/yyyy"));
     
        try {
            this.booking.setPayment(paymentDate, true, this.booking.getTotalHours()*this.car.getPricePerHour(), txtName.getText(), txtCvv.getText(), txtNum.getText(), txtDate.getText());
            this.booking.setRentalStatus(RentalStatus.checkedOut);
            Notification notification = new Notification("Your booking has been successfully made. "
                    + "Please wait for admin approval.", 
                    this.booking.getBookingId());
            this.booking.addNotificationList(notification);
            CarBooking.addBooking(this.booking);

            CustomerRentalStatus rentalStatus = new CustomerRentalStatus(booking);
            rentalStatus.setVisible(true);
            this.setVisible(false);
           
        } catch (IOException ex) {
            Logger.getLogger(CustomerExploreProductCheckout.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
