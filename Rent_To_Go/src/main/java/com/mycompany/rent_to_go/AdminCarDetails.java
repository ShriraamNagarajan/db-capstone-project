   public void fillBookings(Car car) throws IOException{
        DefaultTableModel model = (DefaultTableModel) tableBooking.getModel();

        for(CarBooking booking: CarBooking.getSpecificCarBooking(car)){
                model.addRow(new Object[]{booking.getBookingId(), booking.getRentalStatus(), booking.getPayment().getTotalPayment(), booking.getPickupDate(), booking.getDropOffDate() , booking.getPickupLocation(), booking.getDropOffLocation(), booking.getTotalHours()});

        }
    }
   
