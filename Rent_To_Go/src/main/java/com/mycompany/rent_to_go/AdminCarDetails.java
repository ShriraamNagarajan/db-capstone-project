    public boolean checkEmptyFields(){
        if(txtModel.getText().length()==0 || txtMake.getText().length()==0 || txtPlateNum.getText().length()==0 || txtPasCap.getText().length()==0 || txtLugCap.getText().length()==0 || txtManYear.getText().length()==0 || txtPricePerHour.getText().length()==0){
            return true;
        }
        return false;
    }

    public void saveDetails() throws IOException{
        if(checkEmptyFields()){
                JOptionPane.showMessageDialog(null, "Please make sure that you have entered all the required details", "title", JOptionPane.PLAIN_MESSAGE);
            
        }

        else if(lblPriceError.isVisible() || lblYearError.isVisible() || lblPasError.isVisible()){
                JOptionPane.showMessageDialog(null, "Invalid Details Entered", "title", JOptionPane.PLAIN_MESSAGE);

        }
        else if(!DetailsValidator.weightValidator(txtLugCap.getText())){
                JOptionPane.showMessageDialog(null, "Please enter a valid value for luggage capacity", "title", JOptionPane.PLAIN_MESSAGE);
        }
        else{
            car.setMake(txtMake.getText());
            car.setModel(txtModel.getText());
            car.setPlateNumber(txtPlateNum.getText());
            car.setPassengerCapacity(Integer.valueOf(txtPasCap.getText()));
            car.setLuggageCapacity(Double.valueOf(txtLugCap.getText()));
            car.setTransmission((String)cboTransmission.getSelectedItem());
            car.setPricePerHour(Double.valueOf(txtPricePerHour.getText()));
            car.setManufacturingYear(Integer.valueOf(txtManYear.getText()));
            car.setRentalCenter((String) cboRentalCenter.getSelectedItem());
            
            Car.modifyCar(car);
            
            JOptionPane.showMessageDialog(null, "The car details has been successfully modified", "title", JOptionPane.PLAIN_MESSAGE);

            setNonEditable();
            btnManage.setText("Edit Details");

            
        }
    }
    
    
    
    
